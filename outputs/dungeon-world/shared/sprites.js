// アニメーションする全スプライトを再描画
export function redrawAnimSprites(container) {
	container.querySelectorAll('canvas[data-sprite]').forEach(cv => {
		const frames = SPRITES[cv.dataset.sprite];
		if (!frames) return;
		const pal = PAL[cv.dataset.pal] || PAL.hero;
		if (frames.length > 1) drawSprite(cv, frames, pal);
	});
}
