// ── Animation ─────────────────────────────────────────────────
export let animFrame = 0;
let animTimer = null;
const _tickCallbacks = [];

export function startAnimLoop(onTick) {
	if (onTick) _tickCallbacks.push(onTick);
	if (animTimer) return;
	animTimer = setInterval(() => {
		animFrame = (animFrame + 1) % 2;
		for (const cb of _tickCallbacks) cb();
	}, 400);
}

export function stopAnimLoop() {
	clearInterval(animTimer);
	animTimer = null;
}

// ── Sprite drawing ─────────────────────────────────────────────
export function drawSprite(canvas, frames, palette) {
	const f = animFrame % frames.length;
	const grid = frames[f];
	const rows = grid.length;
	const cols = grid[0].length;
	canvas.width  = cols;
	canvas.height = rows;
	const ctx = canvas.getContext('2d');
	ctx.clearRect(0, 0, cols, rows);
	for (let r = 0; r < rows; r++) {
		for (let c = 0; c < cols; c++) {
			const idx = grid[r][c];
			if (idx === 0) continue;
			ctx.fillStyle = palette[idx] ?? 'transparent';
			ctx.fillRect(c, r, 1, 1);
		}
	}
}

export function makeSprite(spriteName, palName, animated = false) {
	const frames = SPRITES[spriteName];
	if (!frames) return null;
	const cv = document.createElement('canvas');
	cv.className = 'sprite';
	if (animated) {
		cv.dataset.sprite = spriteName;
		cv.dataset.pal    = palName;
	}
	drawSprite(cv, frames, PAL[palName] || PAL.hero);
	return cv;
}

